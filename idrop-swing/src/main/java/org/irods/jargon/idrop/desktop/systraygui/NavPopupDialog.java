    private void btnGoHomeTargetTreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoHomeTargetTreeActionPerformed
        String homeRoot;
        if (idropGui.getiDropCore().getIrodsAccount().isAnonymousAccount()) {
            // log.info("setting home dir to public");
            homeRoot = MiscIRODSUtils.computePublicDirectory(
                    idropGui.getiDropCore().getIrodsAccount());
        } else {
            homeRoot = MiscIRODSUtils
                    .computeHomeDirectoryForIRODSAccount(idropGui.getiDropCore().getIrodsAccount());
        }
        idropGui.getiDropCore().setBasePath(homeRoot);
        idropGui.buildTargetTree(false);
        buildPathTable(homeRoot);

        setVisible(false);
    }//GEN-LAST:event_btnGoHomeTargetTreeActionPerformed

